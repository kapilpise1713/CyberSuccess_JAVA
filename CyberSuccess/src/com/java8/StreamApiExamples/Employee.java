package com.java8.StreamApiExamples;

public class Employee {
	int id;
	String name;
	String address;
	String domain;
	int salary;
	public Employee(int id, String name, String address, String domain, int salary) {
		super();
		this.id = id;
		this.name = name;
		this.address = address;
		this.domain = domain;
		this.salary = salary;
	}
	
	
	

}
